
public class CheckCluster {
	private int indexCluster;
	private int freq;
	
	public CheckCluster(int indexCluster, int freq) {
		super();
		this.indexCluster = indexCluster;
		this.freq = freq;
	}

	public int getIndexCluster() {
		return indexCluster;
	}

	public void setIndexCluster(int indexCluster) {
		this.indexCluster = indexCluster;
	}

	public int getFreq() {
		return freq;
	}

	public void setFreq(int freq) {
		this.freq = freq;
	}
	
	
}
